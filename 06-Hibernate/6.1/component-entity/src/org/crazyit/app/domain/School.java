public class School
{
	//定义该学校实体的id属性
	private Integer id;
	//定义该学校的name属性
	private String name;

	//无参数的构造器
	public School()
	{
	}
	//初始化全部属性的构造器
	public School(String name)
	{
		this.name = name;
	}

	//id属性的setter和getter方法
	public void setId(Integer id)
	{
		this.id = id;
	}
	public Integer getId()
	{
		return this.id;
	}

	//name属性的setter和getter方法
	public void setName(String name)
	{
		this.name = name;
	}
	public String getName()
	{
		return this.name;
	}

}